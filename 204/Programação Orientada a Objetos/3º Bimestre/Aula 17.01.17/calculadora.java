package aula1701;

public class calculadora {
	double x;
	double y;
	
	calculadora(double x, double y){
		this.x=x;
		this.y=y;
	}

		double soma(){
		return x+y;
	}
	
	double subtrair(){
		return x-y;
	}
	
	double multiplicar(){
		return x*y;
	}
	
	double dividir(){
		return x/y;
	}
	
	

}
